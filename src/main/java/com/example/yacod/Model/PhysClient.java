package com.example.yacod.Model;

public class PhysClient {
    public String id;
    public String name;
    public String status;
    public String tel;
    public String bonus;
    public String inn;
    public String mail;

    public PhysClient(String id, String name, String status, String tel, String bonus, String inn, String mail) {
        this.id = id;
        this.name = name;
        this.status = status;
        this.tel = tel;
        this.bonus = bonus;
        this.inn = inn;
        this.mail = mail;
    }
}
